package com.XMLtran;

public class Pair {
	public Pair(String h, String r){
		this.h=h;
		this.r=r;

	}
	private String h;
	private String r;
	
	public String getH(){
		return this.h;
	}
	public String getR(){
		return this.r;
	}
	public void setH(String h){
		this.h=h;
		
	}
	public void setr(String r){
		this.r=r;
		
	}

}
